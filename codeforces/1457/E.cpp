#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
vector<ll> fac;
ll p=1e9+7;
ll gcd(ll x, ll y) {
	if(x==0) {
		return y;
	}
	return gcd(y%x, x);
}
ll power(ll a, ll x) {
	ll ans=1;
	while(x) {
		if(x&1) {
			ans=(ans*a)%p;
		}
		x=x>>1;
		a=(a*a)%p;
	}
	return ans;
}
ll modInv(ll a) {
	return power(a, p-2);
}
ll ncr(ll n, ll r) {
	ll ans=1;	
	if(r!=0 && r!=n) {
		ans=fac[n]*modInv(fac[r])%p*modInv(fac[n-r])%p;
	}
	return ans;
}
void pre(int n) {
	ll x=1;
	fac.push_back(x);
	for(ll i=1; i<=n; i++) {
		x=(x*i)%p;
		fac.push_back(x);
	}
	return;
}
void solve() {
	int n, k;
	cin>>n>>k;
	vector<ll> c(n);
	for(auto &i : c) {
		cin>>i;
	}
	sort(c.rbegin(), c.rend());
	priority_queue<ll> session;
	while(k>=0) {
		session.push(0);
		k--;
	}
	ll ans=0;
	for(auto &boss : c) {
		auto bonus=session.top();
		session.pop();
		ans+=bonus;
		bonus+=boss;
		session.push(bonus);
	}
	cout<<ans<<endl;
    return;
}
